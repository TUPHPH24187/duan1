    HDCTView guidataHDCT() {
        int soLuong = Integer.parseInt(txt_soluong.getText());
        Date date = new Date();
        int indexHoaDon = tb_hoadon.getSelectedRow();
        HoaDon hd = hoaDonService.getList().get(indexHoaDon);
        int maHoaDon = hd.getMaHoaDon();
        int indexSanPham = tb_sanPham.getSelectedRow();
        SanPham sp = sanPhamService.listSanPham().get(indexSanPham);
        String maSanPham = sp.getMaSanPham();
        Float donGia = sp.getGiaBan();
        TheLoaiSanPham loaiSanPham = new TheLoaiSanPham();
        HangSanXuat hangSanXuat = new HangSanXuat();
        KhachHang khachHang = new KhachHang();
        NhanVien nhanVien = new NhanVien();
        HoaDon hoaDon = new HoaDon(maHoaDon, date, khachHang, nhanVien,0);
        SanPham sanPham = new SanPham(maSanPham, "", 1, date, "", 1, 1, 1, "", "", "","","",0, loaiSanPham, hangSanXuat);
        return new HDCTView(-1, donGia, soLuong, (donGia * soLuong), hoaDon, sanPham);
    }
    

    public List<SanPham> timKiem(String ma) {
        List<SanPham> lst = new ArrayList<>();
        for (SanPham x : sanPhamService.listSanPham()) {
            if (x.getMaSanPham().equalsIgnoreCase(ma) || x.getTenSanPham().startsWith(ma)) {
                lst.add(x);
            }
        }
        return lst;
    }

