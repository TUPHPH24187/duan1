    public float inHoaDon() {
        int index = tb_hoadon.getSelectedRow();
        HoaDon hd = hoaDonService.getList().get(index);
        int maHD = hd.getMaHoaDon();
        List<HDCT> list = hDCTService.timKiem(maHD);
        
        float tong = 0;
        for (HDCT x : list) {
            tong+=x.getTongTien();
        }
        return tong;
    }

