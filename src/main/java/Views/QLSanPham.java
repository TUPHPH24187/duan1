    private void tbSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSanPhamMouseClicked
        int row = tbSanPham.getSelectedRow();

        String maGiay = tbSanPham.getValueAt(row, 0).toString();
        String tenGiay = tbSanPham.getValueAt(row, 1).toString();
        String xuatXu = tbSanPham.getValueAt(row, 2).toString();

        cbXuatXu.setSelectedItem(xuatXu);

        String soLuong = tbSanPham.getValueAt(row, 5).toString();
        String gia = tbSanPham.getValueAt(row, 6).toString();
        String giamgia = tbSanPham.getValueAt(row, 7).toString();

        txtMaGiay.setText(maGiay);
        txtTenGiay.setText(tenGiay);
        txtGia.setText(gia);
        txtGiamGia.setText(giamgia);
        txtSoLuong.setText(soLuong);
    }//GEN-LAST:event_tbSanPhamMouseClicked

    private void loadComboBoxXuatXu(ArrayList<XuatXu> list) {
        defaultComboBoxModel = (DefaultComboBoxModel) cbXuatXu.getModel();
        for (XuatXu xuatXu : list) {
            defaultComboBoxModel.addElement(xuatXu);

        }

    }

    private void loadComboBoxChatLieu(ArrayList<ChatLieu> list) {
        defaultComboBoxModel = (DefaultComboBoxModel) cbChatLieu.getModel();
        for (ChatLieu chatLieu : list) {
            defaultComboBoxModel.addElement(chatLieu);

        }

    }

    private void loadComboBoxKichThuoc(ArrayList<KichThuoc> list) {
        defaultComboBoxModel = (DefaultComboBoxModel) cbSize.getModel();
        for (KichThuoc kichThuoc : list) {
            defaultComboBoxModel.addElement(kichThuoc);

        }

    }

    private void loadTable(ArrayList<ChiTietSanPham> list) {
        defaultTableModel = (DefaultTableModel) tbSanPham.getModel();
        defaultTableModel.setRowCount(0);
        for (ChiTietSanPham ctsp : list) {
            defaultTableModel.addRow(new Object[]{
                ctsp.getMaCTSP(), ctsp.getTenCTSP(), ctsp.getXuatXu(), ctsp.getKichThuoc(),
                ctsp.getChatLieu(), ctsp.getSoLuong(), ctsp.getGia(), ctsp.getGiamGia(), ctsp.getTrangThai() == 1 ? "Hoạt động" : "Không hoạt động",});
        }

    }


