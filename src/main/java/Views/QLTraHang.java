    void loadtabe() {
    List<Hienthigannhat> HTDS = service.HienThiHD();

        //Kiểm tra danh sách
        if (HTDS == null) {
            JOptionPane.showMessageDialog(this, "Lỗi");
            return; // kết thúc luôn
        }
        if (HTDS.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Rỗng");
            return; // kết thúc luôn

        }
        //đổ dữ liệu vào bảng
        DefaultTableModel model = (DefaultTableModel) tb_bangHT.getModel();
        model.setColumnCount(0); // xóa cột
        model.addColumn("Mã hóa đơn");
        model.addColumn("Mã nhân viên");
        model.addColumn("Tên Khách Hàng");
        model.addColumn("Thanh toán");
        model.addColumn("Ngày tạo");

        model.addColumn("Ghi chú");
        model.addColumn("Trạng thái");

        model.setRowCount(0);
        for (Hienthigannhat ht : HTDS) {
            Object[] row = new Object[]{ht.getMaHD(), ht.getMaNV(), ht.getTenKH(), ht.getTenHTTT(), ht.getNgayTao(),
                ht.getGhiChu(), ht.TrangThaiHDGH()};
            model.addRow(row);
        }
}
public void loatTabel(){
        defaultTableModel = (DefaultTableModel) tb_bangHT.getModel();
        defaultTableModel.setRowCount(0);
        List<TraHang> ms = hangImpl.getList();
        for (TraHang m : ms) {
        defaultTableModel.addRow(new Object[]{
            m.getTenkhachhang(),m.getMaHD(),m.getNgayTraHang(),m.getTienDaThanhToan(),m.getTongTien()
        });
    }
    }
